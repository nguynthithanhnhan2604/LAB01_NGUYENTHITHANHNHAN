# . Viết chương trình nhập vào 2 số nguyên dương a, b, cho biết kết quả chia lấy phần nguyên và phần dư của a với b
a=int(input("Nhập số nguyên a:"))
b=int(input("Nhập số nguyên b:"))
# chia lấy phần dư
Thuong=a%b
print(" Kết quả chia lấy phần dư:",Thuong)
# Chia lấy phần nguyên
Thuong=a//b
print(" Kết quả chia lấy phần nguyên:", Thuong)
